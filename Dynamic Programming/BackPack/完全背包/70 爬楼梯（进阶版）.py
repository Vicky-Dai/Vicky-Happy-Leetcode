#之前讲这道题目的时候，因为还没有讲背包问题，所以就只是讲了一下爬楼梯最直接的动规方法（斐波那契）。
#我们之前做的 爬楼梯 是只能至多爬两个台阶。
#这次改为：一步一个台阶，两个台阶，三个台阶，.......，直到 m个台阶。问有多少种不同的方法可以爬到楼顶呢？
#这样实际上就变成了一个完全背包题目

class Solution:
    def climbMore(self, m, n):
        #m是物品 n是背包
        dp = [0] * (n+1)
        dp[0] = 1 
        for i in range(1, n+1):
            for j in (1, m+1):
                dp[i] += dp[i-j]
        return dp[n]