# Python实现

def main():
    n = int(input())  # 读取数字的个数
    # 创建哈希表，存储五行数字矩阵与其对应的值
    matrix_map = {
        ("#000#", "#0#0#", "#0#0#", "#0#0#", "#000#"): 0,
        ("##0##", "##0##", "##0##", "##0##", "##0##"): 1,
        ("#000#", "###0#", "#000#", "#0###", "#000#"): 2,
        ("#000#", "###0#", "#000#", "###0#", "#000#"): 3,
        ("#0#0#", "#0#0#", "#000#", "###0#", "###0#"): 4,
        ("#000#", "#0###", "#000#", "###0#", "#000#"): 5,
        ("#000#", "#0###", "#000#", "#0#0#", "#000#"): 6,
        ("#000#", "###0#", "###0#", "###0#", "###0#"): 7,
        ("#000#", "#0#0#", "#000#", "#0#0#", "#000#"): 8,
        ("#000#", "#0#0#", "#000#", "###0#", "#000#"): 9,
    }

    # 用于存储结果的字符串构建器
    ans = []
    
    for _ in range(n):
        matrix = []
        
        for _ in range(5):
            line = input().strip()
            # 替换行中的数字为'0'
            matrix.append(''.join(['0' if char.isdigit() else char for char in line]))
        
        # 查找并添加到结果中
        ans.append(str(matrix_map[tuple(matrix)]))
    
    # 输出最终结果
    print(''.join(ans))

if __name__ == "__main__":
    main()
